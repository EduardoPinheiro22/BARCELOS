# Criando a variável texto e atribuindo o valor 'Olá, laço for.'
texto = 'Olá, laço for.'

# Primeira instrução for para iterar sobre os caracteres da variável texto
for item in texto:
    print('Caractere: ' + item)

# Segunda instrução for para iterar sobre o intervalo de 1 a 10
for numero in range(1, 11):
    print('Número do intervalo: ' + str(numero))
